//! Askamaテンプレート構造体。`templates/layout.html`を継承する各テンプレートは
//! `current_user`フィールドを持つ必要がある。

/// ログイン中のヘッダー(表示名・ログアウトフォーム)を描画するために必要な一式。
///
/// Why: `csrf_token`をテンプレート構造体側の独立したフィールドではなくここに置く。
/// `layout.html`のログアウトフォーム(decision 0021)は`current_user`が`Some`のときだけ
/// 描画されるので、トークンが要るかどうかは認証状態と完全に一致する。両者を別々の
/// フィールドに分けると、「ログイン中ヘッダーを描画するのにトークンは空文字」という
/// 状態を型が許してしまい、そのページのログアウトボタンは必ず403になる。
/// 1つの構造体にまとめることで、その組み合わせを表現できなくする。
pub struct CurrentUser {
    pub display_name: String,
    pub csrf_token: String,
}
